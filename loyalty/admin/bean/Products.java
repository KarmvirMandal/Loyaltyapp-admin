package com.mobiloitte.loyalty.admin.bean;

public class Products {
	private String productId;
	private String bomItemsQuantity;
	private String bomUnit;
	
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getBomItemsQuantity() {
		return bomItemsQuantity;
	}
	public void setBomItemsQuantity(String bomItemsQuantity) {
		this.bomItemsQuantity = bomItemsQuantity;
	}
	public String getBomUnit() {
		return bomUnit;
	}
	public void setBomUnit(String bomUnit) {
		this.bomUnit = bomUnit;
	}
	
	
	
}
